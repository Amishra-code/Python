# decimal ------>> binary
print(bin(25))                               # 0b11001
print(0b0101)                                # 5

# decimal ------>> octal
print(oct(25))                               # 0o31

# decimal ------>> hexadecimal
print(hex(25))                               # 0x19
print(hex(10))                               # 0xa
print(0xf)                                   # 15
